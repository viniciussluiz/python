# Criando tabuada usando for e while
for i in range(1, 11):
    print('3 x {} = {}'.format(i, 3 * i))
print('---')
for i in range(1, 11):
    print('4 x {} = {}'. format(i, 4 * i))
print('---')

numero = 1
while numero <= 10:
    print('3 x {} = {}'.format(numero, 3 * numero))
    numero += 1
print('---')
numero = 1
while numero <= 10:
    print('4 x {} = {}'.format(numero, 4 * numero))
    numero += 1